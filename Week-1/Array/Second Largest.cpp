class Solution {
  public:
    // Function returns the second
    // largest elements
    int getSecondLargest(vector<int> &arr) {
        // Code Here
        int maxval = -1;
        for(auto val : arr){
            maxval = max(maxval,val);
        }
        int semaxval = -1;
        for(auto val: arr){
            if(val < maxval){
                semaxval = max(semaxval,val);
            }
        }
        return semaxval;
    }
};

